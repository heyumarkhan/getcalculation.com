def update_icons():
    """Update all physics calculator icons in calculators.ts"""
    
    filepath = r'e:\Code\getcalculation.com\app\_components\data\calculators.ts'
    
    with open(filepath, 'r', encoding='utf-8') as f:
        content = f.read()
    
    # Track changes
    changed_count = 0
    failed_count = 0
    
    # Find all calculator objects and their IDs
    pattern = r"id:\s*'([^']+)',\n[^\}]*?icon:\s*'[^']*',"
    
    for calc_id, icon in ICON_MAPPING.items():
        # Find the specific calculator entry
        # Look for pattern: id: 'calculator-id', ... icon: '??',
        calc_pattern = f"id:\\s*'{re.escape(calc_id)}',"
        
        if re.search(calc_pattern, content):
            # Find the icon field for this calculator
            # More specific: find from id to icon within a calculator object
            specific_pattern = (
                f"(id:\\s*'{re.escape(calc_id)}',\\s*\\n.*?"
                f"icon:\\s*)'[^']*'(,)"
            )
            
            matches = list(re.finditer(specific_pattern, content, re.DOTALL))
            if matches:
                match = matches[0]  # Get the first (and should be only) match
                old_text = match.group(0)
                new_text = match.group(1) + icon + match.group(2)
                
                content = content.replace(old_text, new_text, 1)
                changed_count += 1
                print(f"‚úì Updated {calc_id} ‚Üí {icon}")
            else:
                failed_count += 1
                print(f"‚úó Failed to find icon field for {calc_id}")
        else:
            # Calculator might not exist, skip
            pass
    
    # Write the updated content back
    with open(filepath, 'w', encoding='utf-8') as f:
        f.write(content)
    
    print(f"\n‚úÖ Updated {changed_count} calculator icons")
    if failed_count > 0:
        print(f"‚ö†Ô∏è  Failed to update {failed_count} calculators")

if __name__ == '__main__':
    update_icons()
